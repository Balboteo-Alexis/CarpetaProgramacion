package ejercicio4;

public class Ejercicio4
{

	public static void main(String[] args)
	{
		
		byte a = 100;
		short b = 200;
		int c = 334212;
		char d = 'D';
		double e = 12.31;
		float f = 23.7653f;
		String g = "una frase cualquiera";
		
		System.out.println("El valor de la variable tipo byte es: "+ a);
		System.out.println("El valor de la variable tipo short es: "+ b);
		System.out.println("El valor de la variable tipo entero es: "+ c);
		System.out.println("El valor de la variable tipo caracter es: "+ d);
		System.out.println("El valor de la variable tipo numero decimal es: "+ e);
		System.out.println("El valor de la variable tipo numero fotante es: "+ f);
		System.out.println("El valor de la variable tipo cadena de carateres es: "+ g);
		
		
		
		
		
		
	}

}
